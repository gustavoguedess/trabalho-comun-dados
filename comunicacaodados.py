def criptografia(s):
    chars = []
    for c in s:
        chars.append(chr(ord(c) + 1))
    return ''.join(chars)[::-1]

def descriptografia(s):
    chars = []
    for c in s[::-1]:
        chars.append(chr(ord(c) - 1))
    return ''.join(chars)

def stringParaBits(s):
    result = []
    for c in s:
        bits = bin(ord(c))[2:]
        bits = '00000000'[len(bits):] + bits
        result.extend([int(b) for b in bits])
    return result

def bitsParaString(bits):
    chars = []
    for b in range(int(len(bits) / 8)):
        byte = bits[b*8:(b+1)*8]
        chars.append(chr(int(''.join([str(bit) for bit in byte]), 2)))
    return ''.join(chars)

